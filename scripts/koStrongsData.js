// 한국어 어절에서 흔한 조사를 떼어내는 함수
// "하나님이" -> "하나님", "여호와께서" -> "여호와", "땅을" -> "땅"
const PARTICLES = [
  '으로부터', '으로써', '에서부터', '에게서', '에게로', '으로서',
  '께서는', '께서도', '께서가', '께서',
  '에서는', '에서도', '에서',
  '으로는', '으로도', '으로',
  '까지', '부터', '마저', '조차',
  '이라도', '이라는', '이라고', '이라', '이며', '이고', '이여', '이오',
  '에는', '에도', '에게', '에서', '에', '의', '와', '과',
  '들이', '들을', '들의', '들은', '들도',
  '들', '은', '는', '이', '가', '을', '를', '도', '만', '라', '여', '야', '아',
  '하사', '하시고', '하시니', '하시며', '하시매', '하시는', '하시니라', '하시었으니',
  '하셨으니', '하셨고', '하셨도다', '하시매',
]

export function stripParticles(word) {
  // 길이가 긴 조사부터 시도
  for (const p of PARTICLES) {
    if (word.length > p.length && word.endsWith(p)) {
      return word.slice(0, word.length - p.length)
    }
  }
  return word
}
